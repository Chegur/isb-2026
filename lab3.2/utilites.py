import os

IV_SIZE = 16
RSA_KEY_SIZE = 2048

def generate_random_bytes(n: int) -> bytes:
    """
    Генерирует криптографически стойкую последовательность случайных байтов.
    
    Использует встроенный генератор случайных чисел операционной системы (os.urandom),
    который собирает энтропию из аппаратных источников (шум устройств, прерывания и т.д.).
    
    Args:
        n (int): Количество случайных байтов, которое необходимо сгенерировать.
                 Должно быть неотрицательным целым числом.
        
    Returns:
        bytes: Объект bytes длиной n, содержащий случайные данные.
        
    Raises:
        ValueError: Если n отрицательно (в некоторых версиях Python/OS).
        OSError: Если система не может собрать достаточное количество энтропии (крайне редко).
    """
    return os.urandom(n)
